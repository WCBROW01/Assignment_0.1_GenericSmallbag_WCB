/**
 * SmallBag class
 * @author Will Brown
 * @version 1.0
 * Fall 2021
 * @param <T>
 */
public class SmallBag<T> {
	
	private T item;

	/**
	 * @return the item
	 */
	public T getItem() {
		return item;
	}

	/**
	 * @param item the item to set
	 */
	public void setItem(T item) {
		this.item = item;
	}

}
